frase="curso de python"
#particionar los strings
print(frase[9])
#recuperar varios caracteres
print(frase[0:5])
#tomar caracteres del 0 al 15 de 2 en 2
print(frase[0:15:2])
#pongo :5 y por defecto comienza en 0
print(frase[:5])
#pongo 6: y comienza desde el 6 hasta el final
print(frase[6:])
#lo mismo de antes per de 3 en 3
print(frase[6::3])

